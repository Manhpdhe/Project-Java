package model;

/**
 *
 * @author HP
 */
public class Classify {
    private Movie movie;
    private Genre genre;

    public Classify() {
    }

    public Classify(Movie movie, Genre genre) {
        this.movie = movie;
        this.genre = genre;
    }

    public Movie getMovie() {
        return movie;
    }

    public void setMovie(Movie movie) {
        this.movie = movie;
    }

    public Genre getGenre() {
        return genre;
    }

    public void setGenre(Genre genre) {
        this.genre = genre;
    }
    
    
    
}
